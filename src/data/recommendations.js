export const getRecommendations = (riskLevel, highestRiskCategory, language = 'en') => {
  const recs = recommendations[language];
  
  if (riskLevel === 'low') {
    return {
      primary: recs.general,
      secondary: null
    };
  }
  
  const primaryCategory = recs[highestRiskCategory] || recs.general;
  const secondaryCategory = riskLevel === 'high' ? recs.general : null;
  
  return {
    primary: primaryCategory,
    secondary: secondaryCategory
  };
};

